//  Relational Operators 
//  are also known as comparision operators , gives result either 0 or 1
//  there should not be space between < & =

#include<stdio.h>
int main()
{
    printf("\n%d",3<4);
    printf("\n%d",5<4);
    printf("\n%d",9>7);
    printf("\n%d",3<=8);
    printf("\n%d",3>=8);
    printf("\n%d",8>=6);
    printf("\n%d",8==8);
    printf("\n%d",9==7);
    printf("\n%d",7!=8);
    return 0;
}